public class Emp {
    private int empid;
    private int empname;
    private int deptid;

    public int getEmpid() {
        return empid;
    }

    public void setEmpid(int empid) {
        this.empid = empid;
    }

    public int getEmpname() {
        return empname;
    }

    public void setEmpname(int empname) {
        this.empname = empname;
    }

    public int getDeptid() {
        return deptid;
    }

    public void setDeptid(int deptid) {
        this.deptid = deptid;
    }
}
